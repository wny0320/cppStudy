#include <iostream>

using namespace std;

int main()
{
	int Number1 = 5;
	int Number2 = 4;
	//+, -, *, /, %
	//이항 연산자
	int Number3 = Number1 + Number2;
	int Number4 = Number1 - Number2;
	int Number5 = Number1 * Number2;
	float Number6 = Number1 / Number2;
	int Number7 = Number1 % Number2;


	cout << Number3 << endl;
	cout << Number4 << endl;
	cout << Number5 << endl;
	cout << Number6 << endl;
	cout << Number7 << endl;
	
	return 0;
}